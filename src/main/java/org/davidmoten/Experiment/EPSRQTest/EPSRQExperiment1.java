package org.davidmoten.Experiment.EPSRQTest;

import org.davidmoten.Scheme.EPSRQ.EPSRQ_Adapter;
import org.davidmoten.SpatialDataProcessor.StaticData.DataSetAccess;

import java.io.BufferedWriter;
import java.io.FileWriter;
import java.util.Random;

/**
 * EPSRQExperiment1:
 * Run the same "Experiment 1" style loops but only for EPSRQ, on the same dataset-generation rules.
 *
 * Baselines to include in paper: DSSE_SKQ and Construction-I (already in your other scripts).
 *
 * Hardware env is printed as reference (paper config), but this runner measures on local machine.
 */
public final class EPSRQExperiment1 {

    public static void main(String[] args) throws Exception {
        // -------------------------
        // Paper environment (print only)
        // -------------------------
        System.out.println("=== Experiment Environment (reference) ===");
        System.out.println("CPU: Intel Xeon Gold 5218R | RAM: 192GB | OS: Ubuntu 20.04 (paper)");
        System.out.println("Implementation: Java | Hash: Blake2b-128 (baselines) | PRF: HmacSHA256 (baselines)");
        System.out.println("Baselines (paper must include): DSSE_SKQ, Construction-I");
        System.out.println();

        // -------------------------
        // Dataset rules (generate in memory)
        // -------------------------
        int W = 8000;          // dictionary size
        int k = 12;            // keywords per object
        String spatialDist = "gaussian";
        int[] powers = {18, 19, 20, 21, 22, 23, 24}; // l
        int[] hsUpdate = {8, 10, 12, 14};            // h: 8..14 (subset, adjust if needed)
        int[] hsSearch = {8, 9, 10, 11, 12};         // h: 8..12
        int[] rPercents = {2, 4, 6, 8, 10, 12};      // R%
        int[] nVals = {2 * 10000, 4 * 10000, 6 * 10000, 8 * 10000, 10 * 10000}; // 2W..10W
        int[] owVals = {500, 1500, 2500, 3500};
        int[] wqVals = {4, 8, 12};

        int warmUp = 2000;
        int formal = 500;
        int totalLoops = warmUp + formal;
        long seed = 2026L;
        Random rnd = new Random(seed);

        try (BufferedWriter out = new BufferedWriter(new FileWriter("result/EPSRQTest_experiment1.txt"))) {
            out.write("### EPSRQ Experiment 1 (EPSRQ only)\n");
            out.write("Dataset: in-memory generated by DataSetAccess\n");
            out.write(String.format("Rules: N=%d (default for Update/Search), grid=[0,2^h), spatial=%s, W=%d Zipf(s=1.0), k=%d\n",
                    100000, spatialDist, W, k));
            out.write("Mapping: h->T, l->gamma\n\n");

            // -------------------------
            // Update phase: vary l and h
            // -------------------------
            out.write("## Update: vary l and h (avg time per update, seconds)\n");
            for (int h : hsUpdate) {
                int edge = 1 << h;
                int n = 100000;
                DataSetAccess ds = new DataSetAccess(h);
                ds.generateFiles(n, 1 << 24); // generate file ids in a large range, then mod by l
                ds.generateKwItems(W, k, n);
                ds.generatePoints(edge, spatialDist, n);

                for (int power : powers) {
                    int l = 1 << power;
                    EPSRQ_Adapter epsrq = new EPSRQ_Adapter(l, h, l, seed);

                    long sumNs = 0;
                    long sumBytes = 0;
                    for (int i = 0; i < n; i++) {
                        long t0 = System.nanoTime();
                        epsrq.update(ds.pointDataSet[i], ds.keywordItemSets[i], "add", new int[]{ds.fileDataSet[i] % l});
                        long t1 = System.nanoTime();
                        sumNs += (t1 - t0);
                        sumBytes += epsrq.getLastUpdateBytes();
                    }
                    double avgSec = (sumNs / 1e9) / n;
                    double avgMB = (sumBytes / 1e6) / n;
                    out.write(String.format("h=%d,l=2^%d,avg_update_sec=%.6f,avg_update_MB=%.6f\n", h, power, avgSec, avgMB));
                    out.flush();
                }
            }
            out.write("\n");

            // -------------------------
            // Search phase sensitivities
            // -------------------------
            out.write("## Search sensitivities (avg time per search, ms) + trapdoor MB\n");

            // fixed baseline params for search
            int fixedH = 12;
            int fixedEdge = 1 << fixedH;
            int fixedL = 1 << 20;
            int fixedN = 100000;
            int fixedRPercent = 3;

            DataSetAccess dsFixed = new DataSetAccess(fixedH);
            dsFixed.generateFiles(fixedN, fixedL);
            dsFixed.generateKwItems(W, k, fixedN);
            dsFixed.generatePoints(fixedEdge, spatialDist, fixedN);

            // build index once for fixed setting
            EPSRQ_Adapter epsrqFixed = new EPSRQ_Adapter(fixedL, fixedH, fixedL, seed);
            for (int i = 0; i < fixedN; i++) {
                epsrqFixed.update(dsFixed.pointDataSet[i], dsFixed.keywordItemSets[i], "add", new int[]{dsFixed.fileDataSet[i]});
            }

            // 1) vary h (8..12)
            out.write("-- vary h (8..12)\n");
            for (int h : hsSearch) {
                int edge = 1 << h;
                DataSetAccess ds = new DataSetAccess(h);
                ds.generateFiles(fixedN, fixedL);
                ds.generateKwItems(W, k, fixedN);
                ds.generatePoints(edge, spatialDist, fixedN);
                EPSRQ_Adapter epsrq = new EPSRQ_Adapter(fixedL, h, fixedL, seed);
                for (int i = 0; i < fixedN; i++) epsrq.update(ds.pointDataSet[i], ds.keywordItemSets[i], "add", new int[]{ds.fileDataSet[i]});

                measureSearch(out, epsrq, ds, h, fixedRPercent, totalLoops, warmUp, formal, rnd);
            }

            // 2) vary l (2^18..2^24), fixed h=12
            out.write("-- vary l (2^18..2^24)\n");
            for (int power : powers) {
                int l = 1 << power;
                DataSetAccess ds = new DataSetAccess(fixedH);
                ds.generateFiles(fixedN, l);
                ds.generateKwItems(W, k, fixedN);
                ds.generatePoints(fixedEdge, spatialDist, fixedN);
                EPSRQ_Adapter epsrq = new EPSRQ_Adapter(l, fixedH, l, seed);
                for (int i = 0; i < fixedN; i++) epsrq.update(ds.pointDataSet[i], ds.keywordItemSets[i], "add", new int[]{ds.fileDataSet[i]});

                measureSearch(out, epsrq, ds, fixedH, fixedRPercent, totalLoops, warmUp, formal, rnd);
            }

            // 3) vary R (2%..12%), fixed l,h,N
            out.write("-- vary R (2%..12%)\n");
            for (int rp : rPercents) {
                measureSearch(out, epsrqFixed, dsFixed, fixedH, rp, totalLoops, warmUp, formal, rnd);
            }

            // 4) vary N (2W..10W), fixed l,h,R
            out.write("-- vary N (2W..10W)\n");
            for (int n : nVals) {
                DataSetAccess ds = new DataSetAccess(fixedH);
                ds.generateFiles(n, fixedL);
                ds.generateKwItems(W, k, n);
                ds.generatePoints(fixedEdge, spatialDist, n);
                EPSRQ_Adapter epsrq = new EPSRQ_Adapter(fixedL, fixedH, fixedL, seed);
                for (int i = 0; i < n; i++) epsrq.update(ds.pointDataSet[i], ds.keywordItemSets[i], "add", new int[]{ds.fileDataSet[i]});
                measureSearch(out, epsrq, ds, fixedH, fixedRPercent, totalLoops, warmUp, formal, rnd);
            }

            // 5) vary ow (EPSRQ ignores state chain; expected nearly flat)
            out.write("-- vary ow (500..3500) [EPSRQ ignores ow by design]\n");
            for (int ow : owVals) {
                // keep same index; just run searches, label ow
                measureSearch(out, epsrqFixed, dsFixed, fixedH, fixedRPercent, totalLoops, warmUp, formal, rnd, "ow=" + ow);
            }

            // 6) vary |WQ| (4,8,12)
            out.write("-- vary |WQ| (4,8,12)\n");
            for (int wq : wqVals) {
                measureSearch(out, epsrqFixed, dsFixed, fixedH, fixedRPercent, totalLoops, warmUp, formal, rnd, "wq=" + wq, wq);
            }

            // -------------------------
            // Communication complexity table (theoretical)
            // -------------------------
            out.write("\n## Communication complexity (theory, Big-O)\n");
            out.write("Scheme,Update(SearchToken),Search(Trapdoor)\n");
            out.write("DSSE_SKQ,O(|P|+|W|),O(|BPC|+|WQ|)\n");
            out.write("Construction-I,O(log(2^h)),O(log(2^h))\n");
            out.write("EPSRQ,O(|W|*gamma) [phantom padding],O(|WQ| + |SGR|)\n");

            out.flush();
        }

        System.out.println("EPSRQ Experiment1 finished. Output: result/EPSRQTest_experiment1.txt");
    }

    private static void measureSearch(BufferedWriter out,
                                      EPSRQ_Adapter epsrq,
                                      DataSetAccess ds,
                                      int h,
                                      int rPercent,
                                      int totalLoops,
                                      int warmUp,
                                      int formal,
                                      Random rnd) throws Exception {
        measureSearch(out, epsrq, ds, h, rPercent, totalLoops, warmUp, formal, rnd, null, 12);
    }

    private static void measureSearch(BufferedWriter out,
                                      EPSRQ_Adapter epsrq,
                                      DataSetAccess ds,
                                      int h,
                                      int rPercent,
                                      int totalLoops,
                                      int warmUp,
                                      int formal,
                                      Random rnd,
                                      String extraLabel) throws Exception {
        measureSearch(out, epsrq, ds, h, rPercent, totalLoops, warmUp, formal, rnd, extraLabel, 12);
    }

    private static void measureSearch(BufferedWriter out,
                                      EPSRQ_Adapter epsrq,
                                      DataSetAccess ds,
                                      int h,
                                      int rPercent,
                                      int totalLoops,
                                      int warmUp,
                                      int formal,
                                      Random rnd,
                                      String extraLabel,
                                      int wqSize) throws Exception {
        int edge = 1 << h;
        int rangeLen = Math.max(1, edge * rPercent / 100);
        int n = ds.pointDataSet.length;

        long sumNs = 0;
        long sumTrapdoorBytes = 0;
        for (int i = 0; i < totalLoops; i++) {
            int x0 = rnd.nextInt(Math.max(1, edge - rangeLen));
            int y0 = rnd.nextInt(Math.max(1, edge - rangeLen));
            int idx = rnd.nextInt(n);
            String[] full = ds.keywordItemSets[idx];
            String[] q = full;
            if (full.length > wqSize) {
                q = new String[wqSize];
                System.arraycopy(full, 0, q, 0, wqSize);
            }

            long t0 = System.nanoTime();
            epsrq.searchRect(x0, y0, rangeLen, q);
            long t1 = System.nanoTime();
            if (i >= warmUp) {
                sumNs += (t1 - t0);
                sumTrapdoorBytes += epsrq.getLastTrapdoorBytes();
            }
        }
        double avgMs = (sumNs / 1e6) / formal;
        double avgTrapdoorMB = (sumTrapdoorBytes / 1e6) / formal;
        String label = (extraLabel == null) ? "" : ("," + extraLabel);
        out.write(String.format("h=%d,R=%d%%,avg_search_ms=%.6f,avg_trapdoor_MB=%.6f%s\n",
                h, rPercent, avgMs, avgTrapdoorMB, label));
        out.flush();
    }
}

